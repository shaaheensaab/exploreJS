// ::::::::::::::WHILE-FOR:::::::::::::::: \\
// while loop..
var roastGiven = 0;
while(roastGiven < 5){
    console.log("Roast den please!!");
    roastGiven++;
}

// for loop..
for(var roastGiven = 0; roastGiven < 5; roastGiven++){
    console.log("Roast den please!");
}

// decent for loop..
for(i = 0; i < 5; i++){
    console.log("That's good!");
}